    private void tabladatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabladatosMouseClicked
      int fila = tabladatos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "fila no seleccionada");
        } else {
            idc = Integer.parseInt((String) tabladatos.getValueAt(fila, 0) .toString());
            String nom = (String) tabladatos.getValueAt(fila, 2);
            String ape = (String) tabladatos.getValueAt(fila, 3);
            String dn = (String) tabladatos.getValueAt(fila, 1);

            id.setText("" + idc);
            id.setEditable(true);
            nombres.setText(nom);
            apellidos.setText(ape);
            dni.setText(dn);

        }        // TODO add your handling code here:
    }//GEN-LAST:event_tabladatosMouseClicked

