    void listar() {
        String sql = "select * from Alumnos";

        try {
            cn = con.getConexion();
            st = cn.createStatement();
            rs = st.executeQuery(sql);

            Object[] alumnos = new Object[4];
            modelo = (DefaultTableModel) tabladatos.getModel();
            while (rs.next()) {
                alumnos[0] = rs.getInt("id");
                alumnos[1] = rs.getString("dni");
                alumnos[2] = rs.getString("nombres");
                alumnos[3] = rs.getString("apellidos");
                modelo.addRow(alumnos);
            }
            tabladatos.setModel(modelo);
        } catch (Exception e) {
            System.out.println("Error en el metodo listar: " + e);
        }
    }
    void agregar() {
        String nom = nombres.getText();
        String ape = apellidos.getText();
        String dn = dni.getText();

        if (nom.equals("") || ape.equals("")) {
            JOptionPane.showMessageDialog(null, "faltan valores");
        } else {
            String sql = "insert into Alumnos (nombres,apellidos,dni) value ('" + nom + "', '" + ape + "',"+ dn + ")";
            try {
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "realizado con exito");
            } catch (Exception e) {
                System.out.println("Error en el metodo agregar: " + e);
            }
        }
    }
    void limpiarTabla() {
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
    void Eliminar() {
        int fila = tabladatos.getSelectedRow();

        try {
            if (fila < 0) {
                JOptionPane.showMessageDialog(null, "esta fila no fue seleccionada");
                limpiarTabla();
            } else {
                String sql = "delete from Alumnos where id=" + idc;
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Eliminado con exito");
                limpiarTabla();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error: " + e);
        }
    }

    void Modificar() {
        String idx = id.getText();
        String nom = nombres.getText();
        String ape = apellidos.getText();
        String dn = dni.getText();

        try {
            if (nom.equals("") || ape.equals("")) {
                JOptionPane.showMessageDialog(null, "Faltan datos");
            } else {
                String sql = "update Alumnos set id = '" + idx + "',nombres = '" + nom + "' , apellidos = '" + ape + "', dni = '" + dn + "' WHERE id = '" + idc + "';";
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Modificado con exito");
                limpiarTabla();
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }

